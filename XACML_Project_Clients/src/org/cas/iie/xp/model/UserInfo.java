package org.cas.iie.xp.model;

public class UserInfo {
	
	private String username;
	
	private String group;
	
	private String resGroup;
	
	private String resAction;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getGroup() {
		return group;
	}

	public void setGroup(String group) {
		this.group = group;
	}

	public String getResGroup() {
		return resGroup;
	}

	public void setResGroup(String resGroup) {
		this.resGroup = resGroup;
	}

	public String getResAction() {
		return resAction;
	}

	public void setResAction(String resAction) {
		this.resAction = resAction;
	}

	

	
	
	

}
